#include "libft.h"

int	ft_atoi(const char *str)
{
	int				a;
	int				b;
	long long int	c;

	a = 0;
	b = 0;
	c = 1;
	while ((str[a] == ' ') || (str[a] >= 9 && str[a] <= 13))
		a++;
	if (str[a] == '-')
		c = -1;
	if (str[a] == '-' || str[a] == '+')
		a++;
	while (str[a] >= '0' && str[a] <= '9')
	{
		b = (str[a] - '0') + b * 10;
		a++;
	}
	return (c * b);
}
